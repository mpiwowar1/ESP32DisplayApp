// ── Query helper ─────────────────────────────────────────────────────────────

/**
 * Return 1 if pixel (col, row) of character `c` is ON, 0 if OFF.
 *
 * @param c    ASCII character (0x20 … 0x5A). Out-of-range → always 0.
 * @param col  Column 0 … 5   (column 5 is the inter-character gap → always 0)
 * @param row  Row    0 … 7   (0 = top of glyph, 7 = bottom)
 */
inline uint8_t menuFontPixel(char c, int col, int row) {
    if (col < 0 || col > 5 || row < 0 || row > 7) return 0;
    if (col == 5) return 0;                        // inter-character gap
    int idx = (int)c - 0x20;
    if (idx < 0 || idx >= (int)(sizeof(MENU_FONT) / sizeof(MENU_FONT[0]))) return 0;
    uint8_t colByte = pgm_read_byte(&MENU_FONT[idx][col]);
    return (colByte >> row) & 0x01;
}